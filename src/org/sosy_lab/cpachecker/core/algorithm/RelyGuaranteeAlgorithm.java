  public Result run(ReachedSet[] reached, boolean stopAfterError) {
    boolean error = false;
    int i = 0;
    int error_i = -1;
    try{
      // fixed point computation
      do {
        setWaitlist(reached[i]);
        addEnvTransitionsToCFA(i);
        error = runThread(i, reached[i], stopAfterError);
        printEnvTransitions();
        processEnvTransitions(i);
        i = pickThread();
      } while(i != -1 && !error);
    } catch(Exception e){
      e.printStackTrace();
    }

    // result analysis
    i=0;
    do {
      if (Iterables.any(reached[i], AbstractElements.IS_TARGET_ELEMENT)) {
        return Result.UNSAFE;
      }
      /*
      if (reached[i].hasWaitingElement()) {
        logger.log(Level.WARNING, "Analysis not completed: there are still elements to be processed.");
        return Result.UNKNOWN;
      }
      if (!sound) {
      logger.log(Level.WARNING, "Analysis incomplete: no errors found, but not everything could be checked.");
      return Result.UNKNOWN;*/

      i++;
    } while (i<this.threadNo );
    return Result.SAFE;
  }


  // put relevant states in the waitlist
  private void setWaitlist(ReachedSet reachedSet) {
    // if waitlist not empty, then this is the first run of for this thread
    if (reachedSet.getWaitlistSize()>0){
      return;
    }
    for(AbstractElement element : reachedSet.getReached()){
      reachedSet.reAddToWaitlist(element);
    }
  }

  private void processEnvTransitions(int i) {
    // generate CFA edges from env tranitions
    Vector<RelyGuaranteeCFAEdge> rgEdges = new Vector<RelyGuaranteeCFAEdge>();
    for (RelyGuaranteeEnvironmentalTransition  et: newEnvTransitions){
      Formula f = et.getFormula();
      PathFormula pf = et.getPathFormula();
      CFAEdge localEdge = et.getEdge();
      int sourceThread = et.getSourceThread();
      // don't generate transition with 'false' abstraction or path formulas
      if (f.isFalse() || pf.getFormula().isFalse()) {
        continue;
      } else {
        PathFormula mergedPF = pfManager.makeAnd(pf, f);
        RelyGuaranteeCFAEdge rgEdge = new RelyGuaranteeCFAEdge(localEdge, mergedPF, sourceThread);
        rgEdges.add(rgEdge);
      }
    }
    // clean newEnvTransitions
    newEnvTransitions.removeAllElements();
    // remove CFA edges that assign to local variables
    removeLocalEdges(rgEdges);
    // remove CFA edges that are covered
    syntacticCoverageCheck(rgEdges,  i);
    if (checkEnvTransitionCoverage) {
      semanticCoverageCheck(rgEdges, i);
    }
    // remember the remaining edges
    envTransitionsCreatedBy[i].addAll(rgEdges);

    // distribute the env edge to other threads
    this.envTransitionsForThread[i].removeAllElements();
    // distribute env. edges
    System.out.println("## Env after filterings ##");
    RelyGuaranteeCFAEdge edge;
    while (!rgEdges.isEmpty()){
      edge = rgEdges.remove(0);
      System.out.println(edge);
      for (int j=0; j<this.threadNo; j++){
        if (j!=i) {
          this.envTransitionsForThread[j].add(new RelyGuaranteeCFAEdge(edge));
        }
      }
    }
  }

  //remove CFA edges that assign to local variables
  // TODO compleate
  private void removeLocalEdges(Vector<RelyGuaranteeCFAEdge> rgEdges) {
    Vector<RelyGuaranteeCFAEdge> toDelete = new Vector<RelyGuaranteeCFAEdge>();
    for (RelyGuaranteeCFAEdge edge : rgEdges) {
      IASTNode node = edge.getLocalEdge().getRawAST();
      if (node instanceof IASTExpressionAssignmentStatement) {
        IASTExpressionAssignmentStatement stmNode = (IASTExpressionAssignmentStatement) node;
        if (stmNode.getLeftHandSide() instanceof IASTIdExpression) {
          IASTIdExpression idExp = (IASTIdExpression) stmNode.getLeftHandSide();
          if(! globalVarsSet.contains(idExp.getName())){
            toDelete.add(edge);
          }
        }

      }
    }
    rgEdges.removeAll(toDelete);
  }


  // removed env edges that are equivalent to edges that have been generated before
  private void syntacticCoverageCheck(Vector<RelyGuaranteeCFAEdge> rgEdges, int i) {
    Vector<RelyGuaranteeCFAEdge> toDelete = new Vector<RelyGuaranteeCFAEdge>();
    // remove env that are false
    for (RelyGuaranteeCFAEdge newEnv : rgEdges) {
      for (RelyGuaranteeCFAEdge oldEnv : this.envTransitionsCreatedBy[i]) {
          if (isSyntacticallyEqual(newEnv, oldEnv)){
            toDelete.add(newEnv);
            System.out.println("Removing (syn) "+newEnv);
        }
      }
    }
    rgEdges.removeAll(toDelete);

  }

  // returns true if env1 is equal to env2 by a quick syntactic check
  private boolean isSyntacticallyEqual(RelyGuaranteeCFAEdge env1, RelyGuaranteeCFAEdge env2) {
    // compare local edges
    if (env1.getLocalEdge() != env2.getLocalEdge()){
      return false;
    }

    // compare the path formulas (without SSAMap)
    Formula f1 = env1.getPathFormula().getFormula();
    Formula f2 = env2.getPathFormula().getFormula();
    if (!f1.equals(f2)){
      return false;
    }
    return true;
  }


  private void semanticCoverageCheck(Vector<RelyGuaranteeCFAEdge> rgEdges, int i) {
    Vector<RelyGuaranteeCFAEdge> toDelete = new Vector<RelyGuaranteeCFAEdge>();
    for (RelyGuaranteeCFAEdge newEdge : rgEdges){
      for (RelyGuaranteeCFAEdge oldEdge : envTransitionsCreatedBy[i]){
        // check for coverage only if the operations are the same
        if (newEdge.getLocalEdge().equals(oldEdge.getLocalEdge())){
          if (isCovered(newEdge, oldEdge)){
            toDelete.add(newEdge);
            break;
          }
        }
      }
    }
  }

  //returns true if env2 is weaker than env1 (env1 => env2)
  private boolean isCovered(RelyGuaranteeCFAEdge env1, RelyGuaranteeCFAEdge env2) {
    Formula f1 = env1.getPathFormula().getFormula();
    Formula f2 = env2.getPathFormula().getFormula();
    Formula nf2 = fManager.makeNot(f2);
    Formula nImpl = fManager.makeAnd(f1, nf2);
    tProver.init();
    try {
      return tProver.isUnsat(nImpl);
    } finally {
      tProver.reset();
    }
  }

  // runs a thread
  private boolean runThread(int i, ReachedSet reached, boolean stopAfterError) throws CPAException, InterruptedException {
    boolean sound = true;
    do {
      sound &=  threadCPA[i].run(reached);
    } while (!stopAfterError && reached.hasWaitingElement());

    if (reached.hasWaitingElement()) {
      return true;
    }
    return false;
  }



